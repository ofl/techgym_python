# データの入れ物となる「変数」を使ってみましょう。


# 1. 変数には文字や数字を「代入」できます。

a = "こんにちは"
print(a)

b = 100
print(b)

# 2. 変数には自分で考えた名前を自由に使えます（ただし英数字と「_」の記号のみ）

mirai365 = "山口市米屋町2-7"

print(mirai365)

# 3. 計算結果も保存しておけます

total = 100 + 12
print(total)

hello = "みなさん" + "こんにちは"
print(hello)

# 4. 変数同士を足したり、連結できます。

c = 10
d = 5
print(c + d)

e = 15
f = 5
g = e + f
print(g)

h = "ここは"
i = "パイソンの"
j = "プログラミング教室です"
print(h + i + j)
